//1. Write a program to find sum of numbers in between 1 to 20?

//Using for
let sum=0
for(let i=1;i<=20;i++)
{
    sum=sum+i;
}
console.log(sum);

//Using While
let j=1
let sum1=0
while(j<=20)
{
    sum1=sum1+j
    j++
}
console.log(sum1)

